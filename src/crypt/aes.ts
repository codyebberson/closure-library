/**
 * Helper to copy input into the AES state matrix.
 * @param {!Array<number>|!Uint8Array} input Byte array to copy into the state
 *     matrix.
 * @private
 */
goog.crypt.Aes.prototype.copyInput_ = function (input) {
  var v, p;

  goog.asserts.assert(input.length == this.BLOCK_SIZE, 'Expecting input of block size.');

  for (var r = 0; r < goog.crypt.Aes.BLOCK_SIZE_IN_WORDS_; r++) {
    for (var c = 0; c < 4; c++) {
      p = c * 4 + r;
      v = input[p];

      goog.asserts.assert(
        v <= 255 && v >= 0,
        'Invalid input. Value %s at position %s is not a byte.',
        v,
        p
      );

      this.state_[r][c] = v;
    }
  }
};

/**
 * Helper to copy the state matrix into an output array.
 * @return {!Array<number>} Output byte array.
 * @private
 */
goog.crypt.Aes.prototype.generateOutput_ = function () {
  var output = [];
  for (var r = 0; r < goog.crypt.Aes.BLOCK_SIZE_IN_WORDS_; r++) {
    for (var c = 0; c < 4; c++) {
      output[c * 4 + r] = this.state_[r][c];
    }
  }
  return output;
};

/**
 * AES's AddRoundKey procedure. Add the current round key to the state.
 * @param {number} round The current round.
 * @private
 */
goog.crypt.Aes.prototype.addRoundKey_ = function (round) {
  for (var r = 0; r < 4; r++) {
    for (var c = 0; c < 4; c++) {
      this.state_[r][c] ^= this.keySchedule_[round * 4 + c][r];
    }
  }
};

/**
 * AES's SubBytes procedure. Substitute bytes from the precomputed SBox lookup
 * into the state.
 * @param {!Array<number>} box The SBox or invSBox.
 * @private
 */
goog.crypt.Aes.prototype.subBytes_ = function (box) {
  for (var r = 0; r < 4; r++) {
    for (var c = 0; c < 4; c++) {
      this.state_[r][c] = box[this.state_[r][c]];
    }
  }
};

/**
 * AES's ShiftRows procedure. Shift the values in each row to the right. Each
 * row is shifted one more slot than the one above it.
 * @private
 */
goog.crypt.Aes.prototype.shiftRows_ = function () {
  for (var r = 1; r < 4; r++) {
    for (var c = 0; c < 4; c++) {
      this.temp_[r][c] = this.state_[r][c];
    }
  }

  for (var r = 1; r < 4; r++) {
    for (var c = 0; c < 4; c++) {
      this.state_[r][c] = this.temp_[r][(c + r) % goog.crypt.Aes.BLOCK_SIZE_IN_WORDS_];
    }
  }
};

/**
 * AES's InvShiftRows procedure. Shift the values in each row to the right.
 * @private
 */
goog.crypt.Aes.prototype.invShiftRows_ = function () {
  for (var r = 1; r < 4; r++) {
    for (var c = 0; c < 4; c++) {
      this.temp_[r][(c + r) % goog.crypt.Aes.BLOCK_SIZE_IN_WORDS_] = this.state_[r][c];
    }
  }

  for (var r = 1; r < 4; r++) {
    for (var c = 0; c < 4; c++) {
      this.state_[r][c] = this.temp_[r][c];
    }
  }
};

/**
 * AES's MixColumns procedure. Mix the columns of the state using magic.
 * @private
 */
goog.crypt.Aes.prototype.mixColumns_ = function () {
  var s = this.state_;
  var t = this.temp_[0];

  for (var c = 0; c < 4; c++) {
    t[0] = s[0][c];
    t[1] = s[1][c];
    t[2] = s[2][c];
    t[3] = s[3][c];

    s[0][c] = goog.crypt.Aes.MULT_2_[t[0]] ^ goog.crypt.Aes.MULT_3_[t[1]] ^ t[2] ^ t[3];
    s[1][c] = t[0] ^ goog.crypt.Aes.MULT_2_[t[1]] ^ goog.crypt.Aes.MULT_3_[t[2]] ^ t[3];
    s[2][c] = t[0] ^ t[1] ^ goog.crypt.Aes.MULT_2_[t[2]] ^ goog.crypt.Aes.MULT_3_[t[3]];
    s[3][c] = goog.crypt.Aes.MULT_3_[t[0]] ^ t[1] ^ t[2] ^ goog.crypt.Aes.MULT_2_[t[3]];
  }
};

/**
 * AES's InvMixColumns procedure.
 * @private
 */
goog.crypt.Aes.prototype.invMixColumns_ = function () {
  var s = this.state_;
  var t = this.temp_[0];

  for (var c = 0; c < 4; c++) {
    t[0] = s[0][c];
    t[1] = s[1][c];
    t[2] = s[2][c];
    t[3] = s[3][c];

    s[0][c] =
      goog.crypt.Aes.MULT_E_[t[0]] ^
      goog.crypt.Aes.MULT_B_[t[1]] ^
      goog.crypt.Aes.MULT_D_[t[2]] ^
      goog.crypt.Aes.MULT_9_[t[3]];

    s[1][c] =
      goog.crypt.Aes.MULT_9_[t[0]] ^
      goog.crypt.Aes.MULT_E_[t[1]] ^
      goog.crypt.Aes.MULT_B_[t[2]] ^
      goog.crypt.Aes.MULT_D_[t[3]];

    s[2][c] =
      goog.crypt.Aes.MULT_D_[t[0]] ^
      goog.crypt.Aes.MULT_9_[t[1]] ^
      goog.crypt.Aes.MULT_E_[t[2]] ^
      goog.crypt.Aes.MULT_B_[t[3]];

    s[3][c] =
      goog.crypt.Aes.MULT_B_[t[0]] ^
      goog.crypt.Aes.MULT_D_[t[1]] ^
      goog.crypt.Aes.MULT_9_[t[2]] ^
      goog.crypt.Aes.MULT_E_[t[3]];
  }
};

/**
 * AES's KeyExpansion procedure. Create the key schedule from the initial key.
 * @private
 */
goog.crypt.Aes.prototype.keyExpansion_ = function () {
  this.keySchedule_ = new Array(goog.crypt.Aes.BLOCK_SIZE_IN_WORDS_ * (this.numberOfRounds_ + 1));

  for (var rowNum = 0; rowNum < this.keyLengthInWords_; rowNum++) {
    this.keySchedule_[rowNum] = [
      this.key_[4 * rowNum],
      this.key_[4 * rowNum + 1],
      this.key_[4 * rowNum + 2],
      this.key_[4 * rowNum + 3],
    ];
  }

  var temp = new Array(4);

  for (
    var rowNum = this.keyLengthInWords_;
    rowNum < goog.crypt.Aes.BLOCK_SIZE_IN_WORDS_ * (this.numberOfRounds_ + 1);
    rowNum++
  ) {
    temp[0] = this.keySchedule_[rowNum - 1][0];
    temp[1] = this.keySchedule_[rowNum - 1][1];
    temp[2] = this.keySchedule_[rowNum - 1][2];
    temp[3] = this.keySchedule_[rowNum - 1][3];

    if (rowNum % this.keyLengthInWords_ == 0) {
      this.rotWord_(temp);
      this.subWord_(temp);

      temp[0] ^= goog.crypt.Aes.RCON_[rowNum / this.keyLengthInWords_][0];
      temp[1] ^= goog.crypt.Aes.RCON_[rowNum / this.keyLengthInWords_][1];
      temp[2] ^= goog.crypt.Aes.RCON_[rowNum / this.keyLengthInWords_][2];
      temp[3] ^= goog.crypt.Aes.RCON_[rowNum / this.keyLengthInWords_][3];
    } else if (this.keyLengthInWords_ > 6 && rowNum % this.keyLengthInWords_ == 4) {
      this.subWord_(temp);
    }

    this.keySchedule_[rowNum] = new Array(4);
    this.keySchedule_[rowNum][0] = this.keySchedule_[rowNum - this.keyLengthInWords_][0] ^ temp[0];
    this.keySchedule_[rowNum][1] = this.keySchedule_[rowNum - this.keyLengthInWords_][1] ^ temp[1];
    this.keySchedule_[rowNum][2] = this.keySchedule_[rowNum - this.keyLengthInWords_][2] ^ temp[2];
    this.keySchedule_[rowNum][3] = this.keySchedule_[rowNum - this.keyLengthInWords_][3] ^ temp[3];
  }
};

/**
 * AES's SubWord procedure.
 * @param {!Array<number>} w Bytes to find the SBox substitution for.
 * @return {!Array<number>} The substituted bytes.
 * @private
 */
goog.crypt.Aes.prototype.subWord_ = (w) => {
  w[0] = goog.crypt.Aes.SBOX_[w[0]];
  w[1] = goog.crypt.Aes.SBOX_[w[1]];
  w[2] = goog.crypt.Aes.SBOX_[w[2]];
  w[3] = goog.crypt.Aes.SBOX_[w[3]];

  return w;
};

/**
 * AES's RotWord procedure.
 * @param {!Array<number>} w Array of bytes to rotate.
 * @return {!Array<number>} The rotated bytes.
 * @private
 */
goog.crypt.Aes.prototype.rotWord_ = (w) => {
  var temp = w[0];

  w[0] = w[1];
  w[1] = w[2];
  w[2] = w[3];
  w[3] = temp;

  return w;
};

